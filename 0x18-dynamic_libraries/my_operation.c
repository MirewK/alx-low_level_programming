#include <stdio.h>
/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the sum of the two numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the difference of the two numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the product of the two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the modulo of the two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
